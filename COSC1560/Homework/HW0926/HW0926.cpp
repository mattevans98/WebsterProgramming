//*******************************************************************************************************

int fun(char *s, char *t)
{
	char *p1,
			*p2;
	bool stop = false;
	for (p1 = s; !stop && *p1 != '\0'; p1++)
	{
		for (p2 = t; !stop && *p2 != '\0'; p2++)
		{
			if (*p1 == *p2)
				stop = true;
		}
		if (*p2 != '\0')
			stop = true;
	}
	return p1 - s;
}

